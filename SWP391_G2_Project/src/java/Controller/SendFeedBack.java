        SendFeedBack_DAO dao = new SendFeedBack_DAO();
        List<Model.ReportType> listC = dao.loadAllReport();
        request.setAttribute("listC", listC);
        
        request.getRequestDispatcher("SendFeedBack.jsp").forward(request, response);
