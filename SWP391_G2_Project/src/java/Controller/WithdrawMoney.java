    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        User_DAO dao1 = new User_DAO();
        Transaction_DAO dao = new Transaction_DAO();
        long money = Integer.parseInt(request.getParameter("money"));
        String uID = request.getParameter("uID");
        boolean recharge = dao1.WithdrawMoney(money, uID);
        String date = request.getParameter("date");

        if (recharge) {
            User user = dao1.getAccountByID(uID);
            request.getSession().setAttribute("user", user);
            dao.insertTransactionHistory(uID, money, 2, date);
        }
        response.sendRedirect("profile");
    }

