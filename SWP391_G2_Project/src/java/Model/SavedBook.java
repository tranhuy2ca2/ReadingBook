package Model;

/**
 *
 * @author ASUS
 */
public class SavedBook {
    private int userid;
    private int bookid;

    public SavedBook() {
    }

    public SavedBook(int userid, int bookid) {
        this.userid = userid;
        this.bookid = bookid;
    }

    public int getUserid() {
        return userid;
    }

    public void setUserid(int userid) {
        this.userid = userid;
    }

    public int getBookid() {
        return bookid;
    }

    public void setBookid(int bookid) {
        this.bookid = bookid;
    }
    
}
